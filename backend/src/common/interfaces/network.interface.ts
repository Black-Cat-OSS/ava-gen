/**
 * Интерфейс для сетевых параметров подключения к базе данных
 *
 * Определяет структуру для настройки сетевого соединения,
 * включая хост, порт и другие сетевые параметры.
 *
 * @interface INetwork
 */
export interface INetwork {
  /** Хост сервера базы данных */
  host: string;

  /** Порт сервера базы данных */
  port: number;
}
